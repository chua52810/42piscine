#!/usr/bin/env python3

int_num1 = int(input("Give me the first number: "))
int_num2 = int(input("Give me the second number: "))
print ("Thank you!")

print(f"{int_num1} + {int_num2} = {int_num1 + int_num2}")
print(f"{int_num1} - {int_num2} = {int_num1 - int_num2}")
print(f"{int_num1} / {int_num2} = {int_num1 // int_num2}")
print(f"{int_num1} * {int_num2} = {int_num1 * int_num2}")
print()
